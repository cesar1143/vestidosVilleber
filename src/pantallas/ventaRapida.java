    private void txtCantidadKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCantidadKeyReleased

        try {
            if (txtPrecio.getText().isEmpty()) {
                txtSubtotal.setText("");
                mensajeAdvertencia mensajeAdver = new mensajeAdvertencia();
                mensajeAdvertencia.labelMensaje.setText("Ingresa el precio");
                mensajeAdver.setVisible(true);
            } else {
                int precio = Integer.parseInt(txtPrecio.getText().toString().trim());

                int cantidad = Integer.parseInt(txtCantidad.getText().toString().trim());
                int subtotal = precio * cantidad;
                txtSubtotal.setText(subtotal + "");
            }

        } catch (Exception e) {
        }

    }//GEN-LAST:event_txtCantidadKeyReleased

